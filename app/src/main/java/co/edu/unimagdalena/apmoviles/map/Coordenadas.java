package co.edu.unimagdalena.apmoviles.map;

public class Coordenadas {

    private int coor1;
    private int coor2;

    public Coordenadas(int coor1, int coor2) {
        this.coor1 = coor1;
        this.coor2 = coor2;
    }

    public int getCoor1() {
        return coor1;
    }

    public void setCoor1(int coor) {
        this.coor1 = coor;
    }

    public int getCoor2() {
        return coor2;
    }

    public void setCoor2(int coor) {
        this.coor2 = coor;
    }


}
